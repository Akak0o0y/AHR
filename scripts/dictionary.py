    import sys, io, argparse
    import paths
    sys.stdout = io.TextIOWrapper(sys.stdout.buffer, encoding='utf-8')

    p = argparse.ArgumentParser()
    sub = p.add_subparsers(dest='cmd', required=True)

    p_fetch = sub.add_parser('fetch',
                             help='Download kaikki Arabic JSONL and convert to '
                                  'flat JSON in one step.')
    p_fetch.add_argument('--out', default=paths.DICTIONARY_JSON)
    p_fetch.add_argument('--jsonl', default=paths.KAIKKI_JSONL,
                         help='Local path for the raw kaikki dump.')
    p_fetch.add_argument('--url', default=KAIKKI_URL_GZ,
                         help='Default is the gzipped .jsonl.gz (~40 MB). '
                              'Pass the plain .jsonl URL if your client '
                              'cannot stream gzip.')
    p_fetch.add_argument('--discard_jsonl', action='store_true',
                         help='Delete the raw JSONL after conversion.')

    p_download = sub.add_parser('download',
                                help='Only download the raw JSONL.')
    p_download.add_argument('--out', default=paths.KAIKKI_JSONL)
    p_download.add_argument('--url', default=KAIKKI_URL_GZ)

    p_kaikki = sub.add_parser('from_kaikki',
                              help='Convert a local kaikki JSONL file → flat JSON.')
    p_kaikki.add_argument('jsonl')
    p_kaikki.add_argument('--out', default=paths.DICTIONARY_JSON)

    p_test = sub.add_parser('test', help='Load a dict and try some lookups.')
    p_test.add_argument('path')

    args = p.parse_args()
    if args.cmd == 'fetch':
        fetch(out_json=args.out, jsonl_path=args.jsonl, url=args.url,
              keep_jsonl=not args.discard_jsonl)
    elif args.cmd == 'download':
        download_kaikki(args.out, args.url)
    elif args.cmd == 'from_kaikki':
        build_from_kaikki(args.jsonl, args.out)
    elif args.cmd == 'test':
        d = ArabicDictionary(args.path)
        for w in ['كتاب', 'مدرسة', 'قمر', 'أزرق', 'ولد', 'غير_موجود']:
            e = d.lookup(w)
            print(f"{w:12s} → {e}")
